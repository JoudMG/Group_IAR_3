    public void OpenReport() {
        JOptionPane.showMessageDialog(this, "Your Reporet Has Been Generated Successfully",
                "Report Generated",
                JOptionPane.INFORMATION_MESSAGE);
        Desktop desktop = Desktop.getDesktop();
        try {
            desktop.open(myObj);
        } catch (IOException ex) {
        }

    }

