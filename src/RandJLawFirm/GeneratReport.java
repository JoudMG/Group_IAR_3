    private void clientReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_clientReportActionPerformed
        PrintWriter writer = null;
        try {
            myObj = new File("Client Report.txt");
            writer = new PrintWriter("Client Report.txt");
            writer.println(Client.GenerateClientsReport());
            writer.flush();
            writer.close();

            // OPEN GENERATED REPORT
            OpenReport();

        } catch (FileNotFoundException ex) {
            Logger.getLogger(GeneratReport.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_clientReportActionPerformed

